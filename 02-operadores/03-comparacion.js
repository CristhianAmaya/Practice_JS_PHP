let a = 10;
// operadores relacionales
console.log(a>5);
console.log(a>=5);
console.log(a<5);
console.log(a<=5);

//operadores de igualdad
console.log(a==10);//Da true
console.log(a!=10);//Da false
console.log(a==='10'); // Cuando tiene tres iguales, pregunta por el tipo de dato y por la igualdad. En este caso daría false porque el tipo de dato es diferente
console.log(a!=='10'); // En este caso daría false porque el valor de a es igual a 10